export const EmojiPicker = ({ onSelectEmoji, onClose }) => {
  const [searchTerm, setSearchTerm] = useState('');
  const [selectedCategory, setSelectedCategory] = useState('Smileys');

  const categories = Object.keys(emojiCategories);

  const filteredEmojis = searchTerm
    ? Object.values(emojiCategories).flat().filter(emoji => emoji.includes(searchTerm))
    : emojiCategories[selectedCategory] || [];

  return (
    <div className="absolute bottom-full right-0 mb-2 w-80 bg-white rounded-2xl shadow-2xl border border-slate-200 overflow-hidden z-50">
      {/* Header */}
      <div className="p-3 border-b border-slate-200 bg-slate-50">
        <div className="flex items-center justify-between mb-2">
          <h3 className="text-sm font-bold text-slate-900">Emojis</h3>
          <button
            onClick={onClose}
            className="w-8 h-8 bg-slate-200 rounded-full flex items-center justify-center hover:bg-slate-300 transition"
          >
            <X className="w-4 h-4 text-slate-600" />
          </button>
        </div>
        
        {/* Busca */}
        <div className="relative">
          <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-slate-400" />
          <input
            type="text"
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            placeholder="Search emoji..."
            className="w-full pl-9 pr-3 py-2 bg-white border border-slate-200 rounded-lg text-sm focus:outline-none focus:ring-2 focus:ring-green-600"
          />
        </div>
      </div>

      {/* Categorias */}
      {!searchTerm && (
        <div className="flex gap-1 p-2 overflow-x-auto bg-white border-b border-slate-200 scrollbar-hide">
          {categories.map((category) => (
            <button
              key={category}
              onClick={() => setSelectedCategory(category)}
              className={`px-3 py-1.5 rounded-lg text-xs font-semibold whitespace-nowrap transition ${
                selectedCategory === category
                  ? 'bg-green-600 text-white'
                  : 'bg-slate-100 text-slate-600 hover:bg-slate-200'
              }`}
            >
              {category}
            </button>
          ))}
        </div>
      )}

      {/* Grid de Emojis */}
      <div className="p-3 h-64 overflow-y-auto">
        {filteredEmojis.length > 0 ? (
          <div className="grid grid-cols-8 gap-2">
            {filteredEmojis.map((emoji, index) => (
              <button
                key={index}
                onClick={() => onSelectEmoji(emoji)}
                className="w-full aspect-square flex items-center justify-center text-2xl hover:bg-slate-100 rounded-lg transition"
              >
                {emoji}
              </button>
            ))}
          </div>
        ) : (
          <div className="flex flex-col items-center justify-center h-full text-slate-400">
            <Search className="w-12 h-12 mb-2" />
            <p className="text-sm">Nenhum emoji encontrado</p>
          </div>
        )}
      </div>

      {/* Emojis Recents/Frequentes */}
      {!searchTerm && (
        <div className="p-3 border-t border-slate-200 bg-slate-50">
          <p className="text-xs font-semibold text-slate-500 mb-2">Usados recentemente</p>
          <div className="flex gap-2">
            {['👍', '❤️', '😊', '🔥', '💰', '📈', '🚀', '💚'].map((emoji, index) => (
              <button
                key={index}
                onClick={() => onSelectEmoji(emoji)}
                className="w-10 h-10 flex items-center justify-center text-xl hover:bg-white rounded-lg transition"
              >
                {emoji}
              </button>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};
