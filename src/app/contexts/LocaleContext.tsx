// Detectar idioma do navegador
const detectBrowserLanguage = (): Language => {
  const browserLang = navigator.language || 'en-US';
  
  if (browserLang.startsWith('pt')) return 'en-US';
  if (browserLang.startsWith('es')) return 'es-ES';
  if (browserLang.startsWith('fr')) return 'fr-FR';
  if (browserLang.startsWith('de')) return 'de-DE';
  if (browserLang.startsWith('it')) return 'it-IT';
  if (browserLang.startsWith('ja')) return 'ja-JP';
  if (browserLang.startsWith('zh')) return 'zh-CN';
  if (browserLang.startsWith('ar')) return 'ar-SA';
  if (browserLang.startsWith('ru')) return 'ru-RU';
  if (browserLang.startsWith('ko')) return 'ko-KR';
  
  return 'en-US';
};

// Detectar região baseado no idioma
const detectRegion = (lang: Language): Region => {
  if (lang === 'pt-BR') return 'BR';
  if (lang === 'en-US') return 'US';
  if (lang === 'es-ES') return 'LATAM';
  if (lang === 'ja-JP') return 'JP';
  if (lang === 'zh-CN') return 'CN';
  return 'GLOBAL';
};

export const LocaleProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [locale, setLocale] = useState<LocaleConfig>(() => {
    try {
      const saved = localStorage.getItem('bullsLocale');
      if (saved) {
        return JSON.parse(saved);
      }
    } catch (e) {
      console.error('Failed to load locale from localStorage', e);
    }
    
    // Default to en-US / IE for European launch
    return {
      language: 'en-US',
      region: 'IE',
      currency: 'EUR',
      marketCode: 'LSE',
    };
  });

  useEffect(() => {
    try {
      localStorage.setItem('bullsLocale', JSON.stringify(locale));
    } catch (e) {
      console.error('Failed to save locale to localStorage', e);
    }
  }, [locale]);

  const setLanguage = (lang: Language) => {
    setLocale(prev => ({ ...prev, language: lang }));
  };

  const setRegion = (region: Region) => {
    const defaults = REGION_DEFAULTS[region];
    setLocale(prev => ({
      ...prev,
      region,
      currency: defaults.currency,
      marketCode: defaults.marketCode,
    }));
  };

  const setCurrency = (currency: Currency) => {
    setLocale(prev => ({ ...prev, currency }));
  };

  const t = (key: string): string => {
    const translation = TRANSLATIONS[locale.language]?.[key];
    if (translation) return translation;
    
    const fallback = TRANSLATIONS['en-US']?.[key];
    if (fallback) return fallback;
    
    return key;
  };

  const formatCurrency = (value: number): string => {
    const currencySymbols: Record<Currency, string> = {
      BRL: 'R$',
      USD: '$',
      EUR: '€',
      GBP: '£',
      JPY: '¥',
      CNY: '¥',
      MXN: 'Mex$',
      ARS: 'AR$',
    };

    const symbol = currencySymbols[locale.currency] || '$';
    
    if (locale.currency === 'JPY' || locale.currency === 'CNY') {
      return `${symbol} ${value.toLocaleString(locale.language, { minimumFractionDigits: 0, maximumFractionDigits: 0 })}`;
    }
    
    return `${symbol} ${value.toLocaleString(locale.language, { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
  };

  const formatNumber = (value: number): string => {
    return value.toLocaleString(locale.language);
  };

  return (
    <LocaleContext.Provider
      value={{
        locale,
        setLanguage,
        setRegion,
        setCurrency,
        t,
        formatCurrency,
        formatNumber,
      }}
    >
      {children}
    </LocaleContext.Provider>
  );
};

export const useLocale = (): LocaleContextType => {
  const context = useContext(LocaleContext);
  if (!context) {
    throw new Error('useLocale must be used within LocaleProvider');
  }
  return context;
};